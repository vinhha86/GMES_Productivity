Ext.define('GSmartApp.store.personnel.Personnel_Position_Store', {
    extend: 'Ext.data.Store',
	alias: 'store.Personnel_Position_Store',
	model: 'GSmartApp.model.personnel.PersonnelModel',

	loadStore(){
		this.setProxy({
			type: 'ajax',
			actionMethods: {
				create : 'POST',
				read   : 'POST',
				update : 'POST',
				destroy: 'POST'
			},
			url: config.getAppBaseUrl()+'/api/v1/personnel_position/load',
			paramsAsJson:true,
			noCache: false,
			headers :{
				'Accept': "application/json", 
				'Content-Type':"application/json"
			 },
			reader: {
				type: 'json',
				rootProperty: 'data'
			}
		});
		this.loadPage(1,{
			scope: this,
			callback: function(records, operation, success) {
				if(!success){
					 // this.fireEvent('logout');
				}
			}
		});
	}
})